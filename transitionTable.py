def get_token_type(lexeme):
    state = 0
    for character in lexeme:
        column = None
        # Finding the column number of the character from the transition table
        for col, col_values in enumerate(TRANSITION_TABLE_MICRO_JAVA[0]):
            if col_values == character:
                column = col
                break
        # Checking if the column was found or not
        if not column:
            return "Unknown Token"
        # Finding the new state to move on
        state = TRANSITION_TABLE_MICRO_JAVA[state + 1][column]
        # Handling Error State
        if state == -1:
            return "Unknown Token"
    # Checking if the token name exists or not
    if TRANSITION_TABLE_MICRO_JAVA[state + 1][1] != -1:
        return TRANSITION_TABLE_MICRO_JAVA[state + 1][1]
    else:
        return "Unknown Token"
